class Operations:

    def sum(self,a,b):
        return a+b

    def sub(self,a,b):
        return a-b
        
    def multiply(self,a,b):
        return a*b

    def div(self,a,b):
        return a/b

op = Operations()

print(op.sum(5,2))
print(op.sub(1,2))
print(op.multiply(1,2))
print(op.div(1,2))
